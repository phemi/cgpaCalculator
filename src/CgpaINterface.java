    private void jMenuItem1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItem1ActionPerformed
        try{
            int noOfCourses = Integer.parseInt(JOptionPane.showInputDialog(null, "Enter the "
                + "number of courses\nYou want to calculate the CGPA:", "Number Of Courses?", 3));
        
        String[] courseCodes = new String[noOfCourses];
        int[] scores = new int[noOfCourses];
        int[] units = new int[noOfCourses];
        int[] points = new int[noOfCourses];
        int[] wGP = new int[noOfCourses];
        String[] remark = new String[noOfCourses];;
        for(int i=0; i<noOfCourses; i++){
            courseCodes[i] = JOptionPane.showInputDialog(null, "Enter the "
                + "Course code"+(i+1)+" :", "Course Code Input", 3);
            
            scores[i]=Integer.parseInt(JOptionPane.showInputDialog(null, "Enter "
                +courseCodes[i]+"'s score:", courseCodes[i]+"'s Score Input", 3));
            
            units[i]=Integer.parseInt(JOptionPane.showInputDialog(null, "Enter "
                +courseCodes[i]+"'s unit:", courseCodes[i]+"'s Unit Input", 3));
                
        }
        
        CGPA bioCGPA = new CGPA();
        
        double gp = bioCGPA.calcGP(scores, units, points, wGP, remark, bioCGPA);
        DecimalFormat dcFormat = new DecimalFormat("0.00");
        
        String result = "CourseCode  SCORE  UNIT  GP  WGP  REMARK\n"; 
        for(int j=0; j<scores.length; j++){
            result+=courseCodes[j]+"                   "+scores[j]+"     "+units[j]+"     "+points[j]+"   "+wGP[j]
                    +"   "+remark[j]+"\n";
        }
        result+="\nTotal Units Taken ="+bioCGPA.returntotalUnits()+"\n";
        result+="Total WGP = "+bioCGPA.returntotalWgp()+"\n";
        result+="Cummulative Grade Point Average = "+dcFormat.format(gp)+"\n";
        result+="\n\n                         CONCEPTS";
        JOptionPane.showMessageDialog(null, result, "TRANSCRIPT?", 1);
        
        }catch(NumberFormatException ex){
            System.err.println("Error!");
        }
    }//GEN-LAST:event_jMenuItem1ActionPerformed

