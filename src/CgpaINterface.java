    private void addCourseButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addCourseButtonActionPerformed
        // TODO add your handling code here:
        try{
            int noOfAdditionalCourse = Integer.parseInt(JOptionPane.showInputDialog(null, "Enter the number of Additional Courses:", "Additional Course", 3));
            String[] additionalCourseTitle= new String[noOfAdditionalCourse];
            String[] additionalCourseCodes = new String[noOfAdditionalCourse];
            int[] additionalScores = new int[noOfAdditionalCourse];
            int[] additionalunits = new int[noOfAdditionalCourse];
        
            for(int i=0; i<noOfAdditionalCourse; i++){
                
                additionalCourseCodes[i] = JOptionPane.showInputDialog(null, "Enter the "
                + "Course code"+(i+1)+" :", "Course Code Input", 3);
                
                additionalCourseTitle[i] = JOptionPane.showInputDialog(null, "Enter "
                +additionalCourseCodes[i]+"'s Title:", "Course Code Input", 3);
            
                additionalScores[i]=Integer.parseInt(JOptionPane.showInputDialog(null, "Enter "
                +additionalCourseCodes[i]+"'s score:", additionalCourseCodes[i]+"'s Score Input", 3));
            
                additionalunits[i]=Integer.parseInt(JOptionPane.showInputDialog(null, "Enter "
                +additionalCourseCodes[i]+"'s unit:", additionalCourseCodes[i]+"'s Unit Input", 3));
            }
            String displayAdditional="";
            for(int j =0 ; j<noOfAdditionalCourse; j++){
                displayAdditional+=additionalCourseTitle[j]+"\t"+additionalCourseCodes[j]+"\t"+additionalScores[j]+"\t"+additionalunits[j]+"\n";
                
            }
            
            textAreaDisplay.setText(displayAdditional);
            textAreaDisplay.setVisible(true);
        }catch(NumberFormatException num){
            
        }
    }//GEN-LAST:event_addCourseButtonActionPerformed

